using System;

namespace Foundation4Encap
{
    public class Address

    {
        private int _streetNumber;
        private string _streetName;
        private string _city;
        private string _stateOrProvince;
        private string _zipCode;

        public Address(int _streetNumber, string _streetName, string _city, string _stateOrProvince, string _zipCode)
        {
            this.streetNumber;
            this._streetName = _streetName;
            this._city = _city;
            this._stateOrProvince;
            this._zipCode = _zipCode;
        }
    }
}
